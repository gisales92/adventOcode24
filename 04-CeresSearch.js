// helper function to check all options
const checkOptions = (xRow, xCol, puzzle) => {
  let count = 0;
  // forward
  let forward = puzzle[xRow].slice(xCol, xCol + 4);
  if (forward === "XMAS") count++;
  // backward
  let backward = puzzle[xRow].slice(xCol - 3, xCol + 1);
  if (backward === "SAMX") count++;
  // up - if we have space to move up
  if (xRow > 2) {
    let upward =
      puzzle[xRow - 1][xCol] + puzzle[xRow - 2][xCol] + puzzle[xRow - 3][xCol];
    if (upward === "MAS") count++;
  }
  // down - if we have space to move down
  if (xRow < puzzle.length - 3) {
    let downward =
      puzzle[xRow + 1][xCol] + puzzle[xRow + 2][xCol] + puzzle[xRow + 3][xCol];
    if (downward === "MAS") count++;
  }
  // diagonals
  if (xCol > 2) {
    if (xRow > 2) {
      let upLeft =
        puzzle[xRow - 1][xCol - 1] +
        puzzle[xRow - 2][xCol - 2] +
        puzzle[xRow - 3][xCol - 3];
      if (upLeft === "MAS") count++;
    }
    if (xRow < puzzle.length - 3) {
      let downLeft =
        puzzle[xRow + 1][xCol - 1] +
        puzzle[xRow + 2][xCol - 2] +
        puzzle[xRow + 3][xCol - 3];
      if (downLeft === "MAS") count++;
    }
  }
  if (xCol < puzzle[xRow].length - 3) {
    if (xRow > 2) {
      let upRight =
        puzzle[xRow - 1][xCol + 1] +
        puzzle[xRow - 2][xCol + 2] +
        puzzle[xRow - 3][xCol + 3];
      if (upRight === "MAS") count++;
    }
    if (xRow < puzzle[xRow].length - 3) {
      let downRight =
        puzzle[xRow + 1][xCol + 1] +
        puzzle[xRow + 2][xCol + 2] +
        puzzle[xRow + 3][xCol + 3];
      if (downRight === "MAS") count++;
    }
  }
  return count;
};

